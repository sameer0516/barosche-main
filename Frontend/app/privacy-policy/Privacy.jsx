const flattenContent = (contentObj) => {
    return Object.keys(contentObj).map(key => contentObj[key]);
};

const rebuildContent = (translationsArr, originalObj) => {
    return Object.keys(originalObj).reduce((acc, key, index) => {
        acc[key] = translationsArr[index];
        return acc;
    }, {});
};

